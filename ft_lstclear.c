#include "libft.h"
#include <stdlib.h>

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp1;
	t_list	*temp2;

	temp1 = *lst;
	if (!*lst)
		return ;
	while (temp1->next != NULL)
	{
		if (temp1->content)
			del(temp1->content);
		temp2 = temp1;
		temp1 = temp1->next;
		free(temp2);
	}
	*lst = NULL;
}
